import os
import re
from abc import ABC, abstractmethod


BatchOutput = list[list[dict[str,str]]]


class DescriptionGenerator(ABC):

    def _build_c_function_prompt(self, c_code: str) -> dict[str, str]:
        """Builds the structured prompt for C function description."""
        system_prompt:str = (
            "You are an expert C code analyst and programmer."
            "Your task is to provide a concise, high-level functional description for the provided C function.\n"
            "KEY: The description must be in plain English prose."
        )
        user_prompt:str = (
            "Generate a description for the following C function.\n\n"
            "**Output requirements**:\n"
            "- The description must be a single, complete paragraph.\n"
            "- Focus only on the function's primary goal.\n"
            "- **DO NOT** mention specific variable names, parameters, or return types.\n"
            "- **DO NOT** use markdown nor any special formatting nor non-ASCII characters in your response.\n\n"
            "--- CODE START ---\n"
            f"{c_code}\n"
            "--- CODE END ---\n\n"
            "**Description:**\n"
        ).strip()

        return { "system": system_prompt, "user": user_prompt }

    def _build_cwe_prompt(self, cwe_id: str) -> dict[str, str]:
        """Builds the structured prompt for CWE description."""

        system_prompt = (
            "You are a cybersecurity expert providing technical summaries.\n"
            "Your task is to define a Common Weakness Enumeration (CWE) identifier.\n"
            "The response must be a direct, factual definition."
        )
        user_prompt = (
            "Define the vulnerability for the following CWE identifier."
            "In case the input doesn't contain any CWE code, answer with an empty string.\n\n"
            "**Output Requirements:**\n"
            "- The response must be a concise, two-sentence definition of the weakness.\n"
            "- Adhere to the exact format shown in the example below.\n"
            "- **DO NOT** use phrases like 'refers to' or 'also known as'.\n"
            "- **DO NOT** provide extra scenarios or mitigation advice.\n\n"
            "- **DO NOT** use any special non-ASCII characters.\n\n"
            "- **DO NOT** use any special formatting or markdown.\n\n"
            "--- EXAMPLE START ---\n"
            "CWE: CWE-20\n"
            "Definition: \"Improper Input Validation\". It highlights an application fails to properly validate user input."
            "This weakness arises when developers do not thoroughly check the type, length, and format of data before it is processed.\n"
            "--- EXAMPLE END ---\n\n"
            f"CWE: {cwe_id}\n"
            "Definition:\n"
        ).strip()

        return { "system": system_prompt, "user": user_prompt }

    def _remove_leading_cwe(self, description: str) -> str:
        """
        Removes a leading "CWE: CWE-XXX Definition: " prefix from a string.

        This function cleans up descriptions that start with a structured
        prefix, including the CWE code and the "Definition:" label.

        Parameters
        ----------
        description : str
            The raw description generated by the language model.

        Returns
        -------
        str
            The cleaned description with the leading prefix removed.
        """

        cleaned_description = re.sub(
            pattern=r'^\s*CWE\s*:\s*CWE-\d+\s*\n?Definition:\s*(?:CWE-\d+\s*)?\s*'
                    r'([^\w\s\"])?',
            repl="",
            string=description,
            count=1,
            flags=re.IGNORECASE,
        )

        return cleaned_description.strip()

    @abstractmethod
    def generate_batch_descriptions(self, c_code_batch: list[str]) -> list[str]:
        """Generates descriptions for a batch of C functions."""
        pass

    @abstractmethod
    def generate_batch_cwe_descriptions(self, cwe_ids_batch: list[str]) -> list[str]:
        """Generates descriptions for a batch of CWE IDs."""
        pass
